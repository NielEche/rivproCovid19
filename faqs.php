                  <h3 class="heading-title style-2 has-text-left">What is COVID-19?</h3>
                    <p class="heading-title-top has-text-left has-text-tertiary">Coronaviruses are zoonotic, meaning they are normally transmitted between animals and people. The coronavirus disease (COVID-19) is caused by a new strain of coronavirus (SARS-CoV-2) that has not been previously identified in humans. It was first reported to WHO on the 31st of December, 2019 in Wuhan, China.</p>
                    <h3 class="heading-title style-2 has-text-left">What is the source of COVID-19?</h3>
                    <p class="heading-title-top has-text-left has-text-tertiary">Scientists and public health officials are working hard to identify the source of the SARS-CoV-2 which is the virus that causes COVID-19. The initial transmission appeared to be from an animal source, but there has been person-to-person transmission in countries.</p>
                    <h3 class="heading-title style-2 has-text-left">Is COVID-19 airborne?</h3>
                        <p class="heading-title-top has-text-left has-text-tertiary"> When a person with coronavirus coughs or exhales, droplets land on objects and surfaces around the person. Other people then catch the virus by touching these objects or surfaces, then touching their eyes, nose or mouth.
                        People can also catch coronavirus if they breathe in these droplets. This is why it is important to stay at least 2 metres away from a person who is coughing or sneezing.</p>
                        <h3 class="heading-title style-2 has-text-left">What are the symptoms of COVID-19?</h3>
                        <li class="heading-title-top has-text-left has-text-tertiary">Cough</li>
                        <li class="heading-title-top has-text-left has-text-tertiary"> Fever</li>
                        <li class="heading-title-top has-text-left has-text-tertiary">Breathing difficulties</li>
                        <p class="heading-title-top has-text-left has-text-tertiary">People who have recently travelled outside Nigeria or have been in contact with a confirmed case of COVID-19 should immediately contact NCDC on 07032864444 or 080097000010 (toll-free) if they feel unwell.</p>
                        
                        <h3 class="heading-title style-2 has-text-left">Are countries with hot weather immune to COVID-19?</h3>
                        <p class="heading-title-top has-text-left has-text-tertiary">From the evidence so far, the virus causing COVID-19 can be transmitted in ALL AREAS, including areas with hot and humid weather.</p>
                        
                        <h3 class="heading-title style-2 has-text-left">Are there any cases in Nigeria?</h3>
                        <p class="heading-title-top has-text-left has-text-tertiary">Yes. Nigeria has recorded confirmed cases of COVID-19. A majority of the cases were imported from high risk countries with community transmission of COVID-19. Measures have been put in place to ensure all confirmed cases are isolated and receive treatment, while intense contact tracing is conducted.</p>
                        
                        <h3 class="heading-title style-2 has-text-left">Has anyone died from the disease in Nigeria?</h3>
                        <p class="heading-title-top has-text-left has-text-tertiary">Yes. Unfortunately, Nigeria has recorded its first fatality from the disease. The case, a Nigerian who had returned from the United Kingdom, had existing medical illnesses which were aggravated by his COVID-19 infection.</p>
                        
                        <h3 class="heading-title style-2 has-text-left">What is Nigeria doing to control the spread of the disease in the country?</h3>
                        <p class="heading-title-top has-text-left has-text-tertiary">The Government of Nigeria, through the Federal Ministry of Health has activated an NCDC-led national COVID-19 Emergency Operations Centre to coordinate response activities and has initiated a Presidential Task Force on COVID-19 to provide high-level strategic national response to the disease in Nigeria.
                            The Federal Ministry of Health is working closely with states and the Presidential Task Force on COVID-19, to review response activities and to institute measures to protect the health of Nigerians.
                            The NCDC has also launched a communication campaign themed #TakeResponsibility. This is a call to all Nigerians and residents in the country to join forces and to be proactive in taking greater individual and collective responsibility in preventing and controlling the spread of COVID-19 in Nigeria.
                            All updates and resources on the disease can be found on www.covid19.ncdc.gov.ng.</p>
                        
                        <h3 class="heading-title style-2 has-text-left">What does self-isolation mean?</h3>
                        <p class="heading-title-top has-text-left has-text-tertiary">Self-isolation is an important way of controlling the spread of COVID-19. It means that anyone who is returning to Nigeria must stay at home and in strict isolation from their families for 14 days. This is to limit contact with people, protect yourself and loved ones. The NCDC guidelines on this can be found HERE</p>
                        
                        <h3 class="heading-title style-2 has-text-left">What does social distancing mean?</h3>
                        <p class="heading-title-top has-text-left has-text-tertiary">This is an effective strategy to reduce physical interaction between people towards delaying and reducing the wide spread of COVID-19 in a community.
                        This measure involves strict adherence to;</p>
                        <li  class="heading-title-top has-text-left has-text-tertiary">Non-contact greetings (avoiding shaking of hands and hugs)</li>
                        <li class="heading-title-top has-text-left has-text-tertiary">Maintaining at least 2 metres (5 feet) physical distance between yourself and individuals and; </li>
                        <li class="heading-title-top has-text-left has-text-tertiary"> Closure of activities that will cause any form of gathering including schools, houses of worship, and social events..</li>
                        
                        <h3 class="heading-title style-2 has-text-left">How are social distancing and self-isolation measures being implemented and monitored?</h3>
                        <p class="heading-title-top has-text-left has-text-tertiary">States Governments, at all levels, are deploying various strategies to ensure compliance with these directives.</p>
                        
                        <h3 class="heading-title style-2 has-text-left">How do I report a person who has refused to self-isolate?</h3>
                        <p class="heading-title-top has-text-left has-text-tertiary">It is expected that every Nigerian resident takes responsibility for controlling the spread of COVID-19. State Governments are also adopting various mechanisms to ensure compliance with instituted measures by the Federal Government including self-isolation. If you know anyone who is not complying with these measures, call your state hotline to enable further investigation.</p>
                       
                        <h3 class="heading-title style-2 has-text-left">Is there a restriction of flights coming from affected countries?</h3>
                        <p class="heading-title-top has-text-left has-text-tertiary"> Yes. The Federal Government of Nigeria has issued a ban on all international flights effective from the 23rd of March 2020 with the exception of emergency and essential flights for an initial period of time.</p>
                        
                        <h3 class="heading-title style-2 has-text-left">Does Nigeria have the capacity to diagnose COVID-19?</h3>
                        <p class="heading-title-top has-text-left has-text-tertiary">Yes. Currently, five laboratories in Nigeria can diagnose COVID-19; NCDC National Reference Laboratory; Virology Laboratory of Lagos University Teaching Hospital; Irrua Specialist Teaching Hospital; Nigerian Institute of Medical Research; and African Centre of Excellence for Genomics of Infectious Diseases. They are all in the NCDC molecular laboratory network. Health officials are advised to call 07032864444 for sample transportation and related advice if they have a case that fits the national case definition.</p>
                    
                        <h3 class="heading-title style-2 has-text-left">How do I get tested for COVID-19?</h3>
                        <p class="heading-title-top has-text-left has-text-tertiary">If you have a recent travel history to any country outside Nigeria or you have been in contact with a confirmed case of COVID-19, you must self-isolate for 14days even if you are not showing symptoms. If you start to feel unwell with the known COVID-19 symptoms during self-isolation, call your state hotline or the NCDC toll-free line immediately on 0800 9700 0010.</p>
                        
                        <h3 class="heading-title style-2 has-text-left">What is the national case definition for COVID-19?</h3>
                        <p class="heading-title-top has-text-left has-text-tertiary">The case definition is continually being reviewed based on new knowledge of the disease. Latest case definition can be found HERE</p>
                        
                        <h3 class="heading-title style-2 has-text-left">Is there a cure for COVID-19?</h3>
                        <p class="heading-title-top has-text-left has-text-tertiary">There is no specific cure yet for COVID-19. However, many of the symptoms can be treated. Treatment is based on the patient’s clinical condition but supportive care for infected persons can be highly effective.</p>
                        
                        <h3 class="heading-title style-2 has-text-left">Where are the treatment centres in Nigeria?</h3>
                        <p class="heading-title-top has-text-left has-text-tertiary">NCDC has conducted rapid assessments of treatment centres. This assessment focused on identifying gaps and developing plans to rapidly ensure that we are prepared to manage cases of COVID-19. Every state in Nigeria has identified a treatment centre with minimum capacity needed to manage a case. This capacity is being scaled up rapidly.
                            Healthcare workers from identified treatment centres are being trained on how to manage COVID-19 cases using approved guidelines.</p>
                        
                        <h3 class="heading-title style-2 has-text-left">What do I do if I cannot get through to NCDC via their numbers?</h3>
                        <p class="heading-title-top has-text-left has-text-tertiary">We are currently receiving a high volume of calls which unfortunately means that you may not be able to reach us easily through our numbers. Alternatively, you may contact us via;
                            <br>SMS: 08099555577
                            <br>WhatsApp: 07087110839
                            <br>Twitter/Facebook: @NCDCgov
                            <br>Email: info@ncdc.gov.ng</p>
                        
                        <h3 class="heading-title style-2 has-text-left">What should I do when I return to Nigeria from a country with a confirmed case(s) of COVID-19?</h3>
                        <p class="heading-title-top has-text-left has-text-tertiary"> All return travellers to Nigeria in the last 14 days must self-isolate by staying indoors and avoiding contact with other people. Please follow this advice even if you do not have symptoms of the virus. If you develop symptoms during this time, immediately contact NCDC on 07032864444 or 080097000010 (toll-free)</p>
                        
                        <h3 class="heading-title style-2 has-text-left">Am I at risk of catching COVID-19 from a package or products that arrive from countries with a confirmed case(s)?</h3>
                        <p class="heading-title-top has-text-left has-text-tertiary"> Currently there is no evidence to support transmission of COVID-19 associated with imported goods or packages countries with confirmed case(s). Till date, there is no known case of COVID-19 in Nigeria associated with imported goods.</p>
                        
                        <h3 class="heading-title style-2 has-text-left">Should I stop my children from going to school?</h3>
                        <p class="heading-title-top has-text-left has-text-tertiary">Yes. The Federal Government and States Governments, at all levels, are taking proactive measures to shut down schools and large gatherings for an initial period of time. They will be monitoring and enforcing compliance.</p>
                        
                        <h3 class="heading-title style-2 has-text-left">Is there anything a person can do to avoid becoming infected?</h3>
                        <p class="heading-title-top has-text-left has-text-tertiary">To reduce the risk of infection or spread of COVID-19, members of the public are advised to adhere to the following hand and respiratory hygiene measures:</p>
                        <li class="heading-title-top has-text-left has-text-tertiary">Wash your hands regularly with soap and water or use an alcohol-based sanitiser if no water and soap is available</li>
                        <li class="heading-title-top has-text-left has-text-tertiary">Use of no-touch greetings</li>
                        <li class="heading-title-top has-text-left has-text-tertiary">Maintain at least 2 metres (5 feet) physical distance between yourself and anyone who is coughing or sneezing</li>
                        <li class="heading-title-top has-text-left has-text-tertiary">Stay home if you feel unwell with symptoms like fever, cough and difficulty in breathing. Immediately call NCDC’s 24/7 toll-free number 0800 970 000 0010. Do not self-medicate</li>
                        <li class="heading-title-top has-text-left has-text-tertiary">Persons with a persistent cough or sneezing, should also stay at home until they recover</li>
                        <li class="heading-title-top has-text-left has-text-tertiary"> Obey national and state directives requesting the cancellation and avoidance of large gatherings, including places of worship, social and sporting events</li>
                        <li class="heading-title-top has-text-left has-text-tertiary">Avoid crowded spaces such as open markets, crowded supermarkets and pharmacies</li>

                        <h3 class="heading-title style-2 has-text-left">What can businesses do to protect their staff?</h3>
                        <p class="heading-title-top has-text-left has-text-tertiary">Businesses have an important role to play in protecting their employees. In line with the directive Employers are advised to circulate NCDC’s public health advisory on COVID-19 to all staff. Encourage sick employees to stay at home if they are unwell. Ensure routine cleaning of high contact areas such as toilets, door handles, telephones. If staff have travelled to any country outside Nigeria in the last 14 days, they must self-isolate for 14 days by staying indoors and avoiding contact with other people.</p>
                    
                        <h3 class="heading-title style-2 has-text-left">What should I do if am pregnant?</h3>
                        <p class="heading-title-top has-text-left has-text-tertiary">Currently, there is no evidence that pregnant women are at higher risk of infection or complications from COVID-19. However, due to changes that may occur during pregnancy, pregnant women are advised to stay at home and adhere strictly to social distancing measures at all times.
Pregnant women are advised to always practice hand and respiratory hygiene to reduce the risk of getting infected with COVID-19 and are advised to call their health facility for more information regarding ante-natal care provision.</p>
