                    <p class="heading-title-top has-text-left has-text-tertiary">The novel coronavirus (SARS-CoV-2) is a new strain of the virus that has not been previously identified in humans. SARS-CoV-2 is the virus that causes coronavirus disease (COVID-19).
                    This is an updated advisory on the coronavirus disease (COVID-19). This advisory will be updated frequently to reflect new information and research emerging on the disease and its impact on populations.
                    The Federal Ministry of Health, through the Nigeria Centre for Disease Control (NCDC), has activated a national Emergency Operations Centre at the highest level and is leading the national response. A National Rapid Response Team has been deployed to support affected State Governments with response activities.
                    Currently, there are five laboratories in NCDC’s molecular laboratory network with the capacity to test for COVID-19 in Nigeria. These laboratories are sufficiently equipped with reagents and supplies for testing.
                    The Federal Government of Nigeria has restricted entry into Nigeria for travellers from the following high incidence countries; China, Italy, Iran, Norway, South Korea, Spain, Japan, France, Germany, United States of America, United Kingdom, Netherlands & Switzerland. ‬Effective from Saturday, 21st of March 2020 for an initial period of four weeks. These are countries with over 1,000 cases domestically. The Federal Government has also suspended the issuance of visa on arrival to travellers from these countries with immediate effect.
                    An intensive communications campaign has begun to provide regular updates and accurate information to Nigerians on protecting themselves. This can be found on www.covid19.ncdc.gov.ng</p>
                    <h3 class="heading-title style-2 has-text-left">TRAVEL ADVICE</h3>
                    <p class="heading-title-top has-text-left has-text-tertiary">Nigerians are strongly advised against non-essential travel to high incidence countries i.e. countries with over 1,000 cases domestically. All returning travellers from foreign countries are required to self-isolate for 14-days. The NCDC has disseminated information on the practice of self-isolation. Additionally, travellers returning from high risk countries will be actively followed-up for 14 days by the NCDC and Port Health Services.
                    The Port Health Services of the Federal Ministry of Health has heightened screening measures at points of entry (Lagos, Kano, Port Harcourt and FCT). In addition to automated temperature screening at points of entry, travellers are given a contact form to fill and asked questions upon arrival about symptoms of illness and travel history. The NCDC is also working with the Nigerian Customs Services to enhance surveillance at land borders to prevent further importation of the disease.</p>
                    <h3 class="heading-title style-2 has-text-left">TRAVELLERS TO NIGERIA</h3>
                        <li class="heading-title-top has-text-left has-text-tertiary">Travellers without symptoms on departure who become unwell in transit should self-report immediately to the Port Health Services on arrival</li>
                        <li class="heading-title-top has-text-left has-text-tertiary">Travel restrictions have been imposed on travellers to Nigeria from China, Italy, Iran, Norway, South Korea, Spain, Japan, France, Germany, United States of America, United Kingdom, Netherlands and Switzerland</li>
                        <li class="heading-title-top has-text-left has-text-tertiary">All travellers returning to Nigeria from any country must self-isolate for 14 days, whether symptomatic or not. If symptoms develop in this time</li>
                        <li class="heading-title-top has-text-left has-text-tertiary">- Call the NCDC 24/7 toll-free line IMMEDIATELY on 080097000010</li>
                        <li class="heading-title-top has-text-left has-text-tertiary">- Avoid self-medication</li>
                        <li class="heading-title-top has-text-left has-text-tertiary">- Avoid contact with people</li>
                        <li class="heading-title-top has-text-left has-text-tertiary">Travellers from high risk countries must</li>
                        <li class="heading-title-top has-text-left has-text-tertiary">- Undergo secondary screening at the point of entry</li>
                        <li class="heading-title-top has-text-left has-text-tertiary">- Self-isolate for 14 days after arrival, even if you do not show symptoms</li>
                        <li class="heading-title-top has-text-left has-text-tertiary">- Be followed up by NCDC and Port Health Services</li>
                        <li class="heading-title-top has-text-left has-text-tertiary">Travellers from Nigeria to these countries are advised to avoid contact with sick people.</li>
                        <h3 class="heading-title style-2 has-text-left">ADVICE FOR HEALTH WORKERS</h3>
                        <p class="heading-title-top has-text-left has-text-tertiary">Healthcare workers are always advised to observe standard infection prevention and control measures when attending to patients and take a travel history. All health workers are strongly advised to maintain a high index of suspicion for COVID-19. Health officials are advised to call 07032864444 for sample transportation and related advice if you have a case that fits the national case definition.</p>
                        <h3 class="heading-title style-2 has-text-left">ADVICE FOR SCHOOLS AND BUSINESSES</h3>
                        <p class="heading-title-top has-text-left has-text-tertiary">The NCDC has published and disseminated guidelines for safe gatherings on how to ensure preventive measures during gatherings. If there is sustained community transmission in Nigeria, various additional measures may be instituted. For now:</p>
                        <li  class="heading-title-top has-text-left has-text-tertiary"> Schools are advised to circulate NCDC’s public health advisory and related materials on COVID-19 to all staff, students and parents</li>
                        <li class="heading-title-top has-text-left has-text-tertiary"> Businesses are advised to circulate NCDC’s public health advisory and related materials on COVID-19 to all employees, clients and visitors</li>
                        <li class="heading-title-top has-text-left has-text-tertiary">Encourage sick employees and students to stay at home if they are unwell.</li>
                        <li class="heading-title-top has-text-left has-text-tertiary"> Ensure routine cleaning of high contact areas such as toilets, door handles, telephones etc.</li>
                        <li class="heading-title-top has-text-left has-text-tertiary">Provide facilities and emphasise the importance of hand washing</li>
                    <h3 class="heading-title style-2 has-text-left">HOW TO PROTECT YOURSELF</h3>
                        <p class="heading-title-top has-text-left has-text-tertiary">To reduce the risk of spread of COVID-19, members of the public are advised to adhere to the following measures:</p>
                        <li class="heading-title-top has-text-left has-text-tertiary">Keep a social distance of least 2 metres while in public and avoid large gatherings and crowded places such as supermarkets and outdoor markets.</li>
                        <li class="heading-title-top has-text-left has-text-tertiary">Wash your hands frequently with soap under running water or use alcohol-based sanitizer if water is not available.</li>
                        <li class="heading-title-top has-text-left has-text-tertiary">Cover your mouth and nose properly with a tissue paper when sneezing and/or coughing. Dispose of the tissue properly immediately after use. You may also cough into your elbow if a tissue is not available.</li>
                        <li class="heading-title-top has-text-left has-text-tertiary">Avoid close contact with anyone showing symptoms of respiratory illness such as coughing and sneezing.</li>
                        <li class="heading-title-top has-text-left has-text-tertiary">Healthcare workers are always advised to observe standard infection prevention and control measures when attending to patients and take a travel history.</li>
                        <p class="heading-title-top has-text-left has-text-tertiary">* Countries with widespread community transmission as at 13th March, 2020: China, Republic of Korea, Italy, Iran, Japan, Spain, France and Germany.</p>
                        <h3 class="heading-title style-2 has-text-left">ABOUT CORONAVIRUS DISEASE (COVID-19)</h3>
                        <p class="heading-title-top has-text-left has-text-tertiary">COVID-19 appears to cause mild to severe respiratory symptoms like fever, cough and difficulty breathing. From current evidence about 98 persons out of 100 persons with the disease will recover. In about 10- 15% of cases, it will cause a severe illness. It appears that death is not a common outcome and occurs mostly in elderly patients with underlying health issues.
                            Currently, four laboratories in Nigeria can diagnose COVID-19. In addition to the NCDC National Reference Laboratory, this includes the Virology Laboratory of Lagos University Teaching Hospital; Irrua Specialist Teaching Hospital and African Centre for Genomics of Infectious Diseases in Ede, Osun State.
                            There is no specific treatment yet for COVID-19. However, many of the symptoms can be treated. Therefore, treatment is based on the patient’s clinical condition. Supportive care for infected persons can be highly effective.</p>
